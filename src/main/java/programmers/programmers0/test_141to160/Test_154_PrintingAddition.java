package programmers.programmers0.test_141to160;

import java.util.Scanner;

public class Test_154_PrintingAddition {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        int c = a + b;

        System.out.println(a + " + " + b + " = " + c);
    }
}
